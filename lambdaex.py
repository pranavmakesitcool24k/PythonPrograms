# create a anonymous function in python @pranav version 3.9
# lambda is a anonymous function

# f is variable and a*a is for finding square of the given no.
# if we have to use a operation multiple times then we use lambda function

f = lambda a: a * a

square = f(5)
a = f(2)
c = f(3)
print(square)
print(a)
print(c)
